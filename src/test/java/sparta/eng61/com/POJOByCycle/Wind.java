
package sparta.eng61.com.POJOByCycle;


public class Wind {


    private Long deg;

    private Long gust;

    private Double speed;

    public Long getDeg() {
        return deg;
    }

    public void setDeg(Long deg) {
        this.deg = deg;
    }

    public Long getGust() {
        return gust;
    }

    public void setGust(Long gust) {
        this.gust = gust;
    }

    public Double getSpeed() {
        return speed;
    }

    public void setSpeed(Double speed) {
        this.speed = speed;
    }

}
